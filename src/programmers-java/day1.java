package programmers;

public class day1 {
	
	// 코딩테스트 입문
	// 1. 두 수의 차
	class Solution {
	    public int solution(int num1, int num2) {
	        int answer = 0;
	        answer = num1 - num2;
	        return answer;
	    }
	}
	
	// 2. 두 수의 합
	class Solution2 {
	    public int solution(int num1, int num2) {
	        int answer = -1;
	        answer = num1 + num2;
	        return answer;
	    }
	}
	
	// 3. 두 수의 곱
	class Solution3 {
	    public int solution(int num1, int num2) {
	        int answer = 0;
	        answer = num1 * num2;
	        return answer;
	    }
	}
	
	// 4. 몫 구하기
	class Solution4 {
	    public int solution(int num1, int num2) {
	        int answer = 0;
	        answer = num1 / num2;
	        return answer;
	    }
	}
	
}
